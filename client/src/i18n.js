// Label lisible pour un groupe ou une phase de la compétition
export function phaseLabel(key, lang) {
  const labels = {
    '8e':     lang === 'fr' ? '8èmes de finale'  : 'Round of 16',
    '4e':     lang === 'fr' ? 'Quarts de finale'  : 'Quarter-finals',
    'demi':   lang === 'fr' ? 'Demi-finales'      : 'Semi-finals',
    'finale': lang === 'fr' ? 'Finale'             : 'Final',
  }
  return labels[key] ?? `${t(lang, 'groupLabel')} ${key}`
}
